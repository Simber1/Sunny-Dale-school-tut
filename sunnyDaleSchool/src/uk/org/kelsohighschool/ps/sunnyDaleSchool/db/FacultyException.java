package uk.org.kelsohighschool.ps.sunnyDaleSchool.db;

public class FacultyException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 8411763231555396055L;

	public FacultyException() {
	}

	public FacultyException(String arg0) {
		super(arg0);
	}

	public FacultyException(Throwable arg0) {
		super(arg0);
	}

	public FacultyException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public FacultyException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
