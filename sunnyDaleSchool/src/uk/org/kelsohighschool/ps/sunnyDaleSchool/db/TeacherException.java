package uk.org.kelsohighschool.ps.sunnyDaleSchool.db;

public class TeacherException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 2068761186595942085L;
	
	public TeacherException() {
	}

	public TeacherException(String arg0) {
		super(arg0);
	}

	public TeacherException(Throwable arg0) {
		super(arg0);
	}

	public TeacherException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public TeacherException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
